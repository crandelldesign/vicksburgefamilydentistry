                    <h2>About Us</h2>
                    <h3>Dr. Aaron C. Ford</h3>
                    <p>Dr. Ford grew up in Sturgis, Michigan and earned his bachelors degree from Western Michigan University in 1999.  While at Western, he was involved in the Army ROTC program.  After graduation, Dr. Ford was commissioned in the United States Army as a Second Lieutenant.  He served two years at Fort Hood, Texas in the 1st Calvary Division where he was a medical platoon leader in the Medical Service Corps.  His next assignment took him to Walter Reed Army Medical Center in Washington, DC, where he spent the next two years in the Health Physics Department.</p>
                    <p>After his commitment with the Army, Dr. Ford entered dental school in 2003 at the University of Michigan and graduated in 2007.  He moved to Vicksburg in 2009 and really enjoys living in south west Michigan.  Dr. Ford is married to Maureen McGlinchey Ford and they have three children.</p>
                    <p>He is a member of the American Dental Association, Michigan Dental Association, and Kalamazoo Valley District Dental Society.  He participates in many continuing education courses throughout the year.</p>

                    <h3>Office Staff</h3>
                    <p>The Office is managed by Kris McGlothlen who brings in 19 years of dental experience. The front desk is ran by Dawn.  She is joined by Brenda and Cheryl, who will greet you with a smile and are very willing to help you with your insurance and billing questions.</p>
